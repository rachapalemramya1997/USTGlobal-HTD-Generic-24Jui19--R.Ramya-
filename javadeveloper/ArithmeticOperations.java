public class ArithmeticOperations{
public static void main(String []args){
int i=10;
int j=20;
int  res=i+j;			//reinitialize variable res many times.
System.out.println("+ Result = "+res);	//addition
res=i-j;
System.out.println("+ Result = "+res);	//subtraction
res=i*j;
System.out.println("+ Result = "+res);	//multiplication
res=i/j;
System.out.println("+ Result = "+res);	//division
res=i%j;
System.out.println("+ Result = "+res);	//modulo division
char a,c;
a='A';
c='B';
res=a+c;
System.out.println("+ Result = "+res);	//characters addition
res=a-c;
System.out.println("+ Result = "+res);	//character subtraction
res=a*c;
System.out.println("+ Result = "+res);	//character multiplication
res=a/c;
System.out.println("+ Result = "+res);	//char division
res=a%c;
System.out.println("+ Result = "+res);	//char modulo division
}}