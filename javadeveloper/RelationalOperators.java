public class RelationalOperators{
public static void main(String [] args){
int i = 10;
int j = 20; 
boolean b= i ==j;
System.out.println("For ==  : "+b);
b= i !=j;
System.out.println("For != : "+b);
b= i <=j;
System.out.println("For <= : "+b);
b= i >=j;
System.out.println("For >= : "+b);
b= i < j;
System.out.println("For < : "+b);
b= i >j;
System.out.println("For > : "+b);
}}